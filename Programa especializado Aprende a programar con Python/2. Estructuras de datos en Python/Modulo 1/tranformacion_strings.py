s1 = "Prueba"
s2 = "prueba"
s3 = "   prueba   "
s4 = "Hola Mundo"

#Capitalizar cadena de texto
s2.capitalize()

#convertir en minusculas todos los caracteres de la cadena de caracteres.
s1.lower()

#convertir en mayusculas todos los caracteres de la cadena de caracteres.
s1.upper()

#sacar los espacios al comienzo y al final de la cadena de caracteres,
s3.strip()

#dividir la cadena de caracteres segun la cadena de caracteres pasado como parametro
s4.split(" ")

#intercambia las mayusculas por las minusculas y viceversa
s1.swapcase

#le pone formato titulo a la cadena de caracteres.
s1.title()

#quita los espacios en blanco de la derecha
s3.rstrip()

#quita los espacios en blanco de la izquierda
s3.lstrip()

#justifica la cadena de texto a la derecha con una longuitud de tantos caracteres como se indica en e parametro
s1.rjust(10)

#justifica la cadena de texto a la izquierda con una longuitud de tantos caracteres como se indica en e parametro
s1.ljust(10)

#reemplaza una cadena de texto por otra en la cadena de texto
s2.replace("r","h")

#centrar la cadena de texto tomando como longituf la cadena de caracteres indicada como parametro
s1.center(10)

#completa la cadena de texto con ceros tomando como longitud la cantidad de caracteres indicada como paramentro 
s1.zfill(10)

